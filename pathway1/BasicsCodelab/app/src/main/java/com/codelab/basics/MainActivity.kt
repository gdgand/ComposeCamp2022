@Composable
fun Greeting(name: String) {
    var expanded by remember { mutableStateOf(false) }

    val extraPadding by animateDpAsState(
        targetValue = if (expanded) 100.dp else 50.dp,
        animationSpec = spring(
            dampingRatio = Spring.DampingRatioHighBouncy,
            stiffness = Spring.StiffnessLow
        )
    )

    if (expanded) 48.dp else 0.dp

    Card(
        backgroundColor = MaterialTheme.colors.primary,
        modifier = Modifier.padding(vertical = 4.dp, horizontal = 8.dp)
    ) {
        Row(modifier = Modifier.padding(24.dp)) {
            Column(
                modifier = Modifier
                    .weight(1f)
                    .animateContentSize(
                        animationSpec = spring(
                            dampingRatio = Spring.DampingRatioHighBouncy,
                            stiffness = Spring.StiffnessLow
                        )
                    )
            ) {
                Text(text = "Hello ")
                Text(text = "$name", style = MaterialTheme.typography.h2)
                if (expanded) Text(text = "배고파배고파배고파배고파배고파배고파배고파배고파배고파배고파배고파")
            }

            IconButton(
                onClick = { expanded = !expanded }
            ) {
                Icon(
                    imageVector = if (expanded) Icons.Filled.ExpandLess else Icons.Filled.ExpandMore,
                    contentDescription = if (expanded) stringResource(R.string.text_show_less) else stringResource(R.string.text_show_more)
                )
            }
        }
    }
}

@Composable
private fun MyApp(modifier: Modifier = Modifier, names: List<String> = listOf("World", "Compose")) {
    var shouldOnBoarding by rememberSaveable { mutableStateOf(true) }

    androidx.compose.material.Surface(modifier) {
        if (shouldOnBoarding) {
            OnboardingScreen(onBoardingClicked = { shouldOnBoarding = !shouldOnBoarding })
        } else {
            Greetings()
        }
    }
    Greetings()
}

@Composable
private fun Greetings(
    modifier: Modifier = Modifier,
    names: List<String> = List(1000) { "$it" }
) {
    LazyColumn(modifier = modifier.padding(vertical = 4.dp)) {
        items(items = names) { name ->
            Greeting(name = name)
        }
    }
}

@Composable
fun OnboardingScreen(modifier: Modifier = Modifier, onBoardingClicked: () -> Unit) {
    Column(
        modifier = modifier.fillMaxSize(),
        verticalArrangement = Arrangement.Center,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        Text("Welcome to the Basics Codelab!")
        IconButton(
            modifier = Modifier.padding(vertical = 24.dp),
            onClick = { onBoardingClicked.invoke() }
        ) {
            Text("Continue")
        }
    }
}

